-- ─── Locale helper ────────────────────────────────────────────────────────────
function Locale(key, subs)
    local lang      = Config.Language or "en"
    local translate = (Config.Locale[lang] and Config.Locale[lang][key])
                   or (Config.Locale["en"] and Config.Locale["en"][key])
                   or ("?" .. key .. "?")
    subs = subs or {}
    for k, v in pairs(subs) do
        local pattern   = '%${' .. k .. '}'
        local safeValue = tostring(v):gsub("%%", "%%%%")
        translate       = translate:gsub(pattern, safeValue)
    end
    return tostring(translate):gsub("%%%%", "%%")
end
